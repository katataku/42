#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ptr;

	if (s == NULL)
		return (NULL);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	ptr = (char *)ft_calloc(sizeof(char), (len + 1));
	if (ptr != NULL)
		ft_strlcpy(ptr, (char *)s + start, len + 1);
	return (ptr);
}
