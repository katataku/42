#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*index;

	index = (char *)s + ft_strlen((char *)s) + 1;
	while (TRUE)
	{
		if (*index == (char)c)
			return (index);
		if (index == s)
			return (NULL);
		index--;
	}
}
