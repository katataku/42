#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*tmp;

	if (count == 0 || size == 0)
		return ((void *)malloc(1));
	if (size * count < 0)
		return (NULL);
	tmp = (void *)malloc(size * count);
	if (tmp != NULL)
		ft_memset(tmp, '\0', size * count);
	return (tmp);
}
