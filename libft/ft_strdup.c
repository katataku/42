#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	size;
	char	*tmp;

	size = (ft_strlen(s1) + 1);
	tmp = (char *)malloc(size * sizeof(char));
	if (tmp != NULL)
		ft_strlcpy(tmp, (char *)s1, size);
	return (tmp);
}
